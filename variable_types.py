# Python 变量和数据类型

# 整数
age = 25
print(f"年龄: {age}, 类型: {type(age)}")

# 浮点数
price = 19.99
print(f"价格: {price}, 类型: {type(price)}")

# 字符串
name = "Python"
print(f"名字: {name}, 类型: {type(name)}")

# 布尔值
is_active = True
print(f"激活: {is_active}, 类型: {type(is_active)}")

# 列表
numbers = [1, 2, 3, 4, 5]
print(f"列表: {numbers}, 类型: {type(numbers)}")

# 元组
point = (10, 20)
print(f"元组: {point}, 类型: {type(point)}")

# 集合
unique_nums = {1, 2, 3}
print(f"集合: {unique_nums}, 类型: {type(unique_nums)}")

# 字典
person = {"name": "张三", "age": 25}
print(f"字典: {person}, 类型: {type(person)}")

# 类型转换
num_str = "42"
num_int = int(num_str)
print(f"字符串转整数: {num_int}")
